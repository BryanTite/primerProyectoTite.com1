            <h2>1.- INFORMACIÓN AL USUARIO</h2>
            <p>
            Una cookie es un archivo de texto que un servidor web puede guardar en el disco duro de tu equipo para almacenar algún tipo de información sobre ti como usuario. Las cookies se utilizan con diversas finalidades tales como almacenar datos para próximas visitas, para reconocer al usuario y evitar pedir de nuevo la autentificación, para saber que paginas visitas, o para guardar tus preferencias en áreas personalizables…). Normalmente los sitios web utilizan las cookies para obtener información estadística sobre sus páginas web, y analizar el comportamiento de sus clientes/usuarios.
            
            <br>
            Quedan exceptuadas del cumplimiento de las obligaciones establecidas en el artículo 22.2 de la LSSI las cookies utilizadas para alguna de las siguientes finalidades:
            </p>

            <ul>
                <li>Permitir únicamente la comunicación entre el equipo del usuario y la red.</li>
                <li>Estrictamente prestar un servicio expresamente solicitado por el usuario.
            </ul>

            <p>
            Así, quedan entre las cookies exceptuadas estarían aquellas que tienen por finalidad:
            </p>

            <ul>
                <li>Cookies de “entrada del usuario”.</li>
                <li>Cookies de autenticación o identificación de usuario (únicamente de sesión).</li>
                <li>Cookies de seguridad del usuario.</li>
                <li>Cookies de sesión de reproductor multimedia.</li>
                <li>Cookies de sesión para equilibrar la carga.</li>
                <li>Cookies de personalización de la interfaz de usuario.</li>
                <li>Determinadas cookies de complemento (plug-in) para intercambiar contenidos sociales.</li>
                <li>Los usos concretos que hacemos de estas tecnologías se describen en los siguientes apartados.</li>

            </ul>

            <br>
            <h2>2.- TIPOS DE COOKIES</h2>

            <p>
            Las cookies pueden clasificarse según los siguientes criterios:
            <br>
            Según quien instala la cookie, éstas pueden ser:
            <br><br>
            <b>Cookies propias:</b> Son aquéllas que se envían al equipo terminal del usuario desde un equipo o dominio gestionado por el propio editor y desde el que se presta el servicio solicitado por el usuario.
            <br><br>
            <b>Cookies de terceros:</b> Son aquéllas que se envían al equipo terminal del usuario bien desde un desde un equipo o dominio gestionado por nosotros o por un tercero, pero la información que se recoja las cookies es gestionada por un tercero distinto del titular de la web.
            <br><br>
            Según su plazo de conservación, las cookies pueden ser:
            <br><br>
            <b>Cookies de sesión:</b> Son aquellas que duran el tiempo que el usuario está navegando por la página Web y se borran cuando finaliza la navegación.
            <br><br>
            <b>Cookies persistentes:</b> Quedan almacenadas en el terminal del usuario, por un tiempo más largo, facilitando así el control de las preferencias elegidas sin tener que repetir ciertos parámetros cada vez que se visite el sitio Web.
            <br><br>
            Según su finalidad, las cookies pueden ser:
            <br><br>
            <b>Cookies técnicas:</b> Las cookies técnicas son aquellas imprescindibles y estrictamente necesarias para el correcto funcionamiento de un sitio Web y el uso de las diversas opciones y servicios que ofrece. Por ejemplo, las de mantenimiento de sesión, las que permiten utilizar elementos de seguridad, compartir contenido con redes sociales, etc.
            <br><br>
            <b>Cookies de personalización:</b> Permiten al usuario escoger o personalizar características de la página Web como el idioma, configuración regional o tipo de navegador.
            </p>

